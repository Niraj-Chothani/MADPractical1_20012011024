fun main()
{
    var x=10
    println("integer value :$x")
    println("Double Value (From integer) : " + x.toDouble())

    var y= "10"
    println("String value : $y")
    println("Integer value (From String) : " + y.toInt())

    var z= "10"
    println("String value : $z")
    println("Double value (From String) : " + z.toDouble())
}